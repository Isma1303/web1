                    <h2>Filosofía</h2>
                    <p>La Universidad San Pablo de Guatemala se fundamenta en un sistema educativo a partir del cual se diseñan modelos innovadores por medio de los cuales la acción pedagógica hace compromiso con el prójimo; para llegar a ser asumida como el medio de transformación personal y social. El sistema educativo se funda en la verdad y procede por la convicción ética de quienes la practicán en la orientación hacia la consecución del progreso y el bienestar de la comunidad. Por ende, es una institución comprometida con la sociedad, en la generación y transmisión de conocimientos, educando en la excelencia, para contribuir en la construcción de una vida cotidiana, que se fundamenta en que el cristianismo tenga un alto impacto social, basándose en la ética, judeo-cristiana, las normas morales y los valores que reconocen al prójimo.</p>
