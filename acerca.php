                <h2>Valores</h2>
                <p>
                  <ul>
                    <li><b>Libertad:</b> es el derecho por naturaleza de libre albedrío con responsabilidad, en búsqueda de la propia realización personal y social. El resultado serán estudiantes y profesionales responsables y respetuosos de toda expresión de libertad.</li>
                    <li><b>Prosperidad:</b> es un estado de bienestar integral en el ser humano. Por ende, los estudiantes y profesionales generarán desarrollo y bienestar a su sociedad.</li>
                    <li><b>Justicia:</b> es la virtud que regula la igualdad y da a cada uno lo que le pertenece. La comunidad universitaria cohabitará en un sistema justo y equitativo.</li>
                    <li><b>Liderazgo progresista:</b> se establece como una influencia innovadora, positiva y comprometida con la excelencia. Cada miembro de la comunidad universitaria será un reflejo de este liderazgo.</li>
                    <li><b>Responsabilidad:</b> es reconocer y aceptar las consecuencias de un hecho realizado libremente. Asumida por toda la comunidad universitaria, al constituirse en dos aspectos esenciales: equidad y desarrollo de la sociedad. Integridad: es la práctica de la rectitud en el ejercicio de la educación y la convivencia. Estudiantes y profesionales rectos, probos en el ejercicio de sus actos, serán el resultado de la práctica de este valor.</li>
                    <li><b>Servicio:</b> es la entrega a determinado fin, a algo positivo y superior del interés individual, al cumplimiento del deber y al cumplimiento de las responsabilidades de la comunidad universitaria. Proyectándose el mismo hacia la sociedad.</li>
                  </ul>
